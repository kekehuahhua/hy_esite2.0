package project.caidan.model;


public class CDLottery
{
	private long id;
	private String img;
	private long lid;
	private int idx;
	private long pageid;
	
	public long getId()
	{
		return id;
	}
	
	public void setId(long id)
	{
		this.id = id;
	}
	
	public String getImg()
	{
		return img;
	}
	
	public void setImg(String img)
	{
		this.img = img;
	}
	
	public long getLid()
	{
		return lid;
	}
	
	public void setLid(long lid)
	{
		this.lid = lid;
	}
	
	public int getIdx()
	{
		return idx;
	}
	
	public void setIdx(int idx)
	{
		this.idx = idx;
	}
	
	public long getPageid()
	{
		return pageid;
	}
	
	public void setPageid(long pageid)
	{
		this.pageid = pageid;
	}
	
	
}
