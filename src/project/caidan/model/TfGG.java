package project.caidan.model;


public class TfGG
{
	private long id;
	private long ggid;
	private int idx;
	private int type;
	private long owner;
	
	public long getId()
	{
		return id;
	}
	
	public void setId(long id)
	{
		this.id = id;
	}
	
	public long getGgid()
	{
		return ggid;
	}
	
	public void setGgid(long ggid)
	{
		this.ggid = ggid;
	}
	
	public int getIdx()
	{
		return idx;
	}
	
	public void setIdx(int idx)
	{
		this.idx = idx;
	}
	
	public long getOwner()
	{
		return owner;
	}
	
	public void setOwner(long owner)
	{
		this.owner = owner;
	}

	
	public int getType()
	{
		return type;
	}

	
	public void setType(int type)
	{
		this.type = type;
	}
	
	
}
