package project.caidan.model;


public class CDSet
{
	private long id;
	private long enid;
	private String type;
	private String img;
	
	public long getId()
	{
		return id;
	}
	
	public void setId(long id)
	{
		this.id = id;
	}
	
	public long getEnid()
	{
		return enid;
	}
	
	public void setEnid(long enid)
	{
		this.enid = enid;
	}
	
	public String getType()
	{
		return type;
	}
	
	public void setType(String type)
	{
		this.type = type;
	}
	
	public String getImg()
	{
		return img;
	}
	
	public void setImg(String img)
	{
		this.img = img;
	}
	
	
}
