package com.huiyee.interact.yuyue.model;


public class YuYueTag
{
	private long id;
	private String name;
	private long yyid;
	
	public long getId()
	{
		return id;
	}
	
	public void setId(long id)
	{
		this.id = id;
	}
	
	public String getName()
	{
		return name;
	}
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	public long getYyid()
	{
		return yyid;
	}
	
	public void setYyid(long yyid)
	{
		this.yyid = yyid;
	}
	
	
}
