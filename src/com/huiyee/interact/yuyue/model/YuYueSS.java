package com.huiyee.interact.yuyue.model;


public class YuYueSS
{
	private long id;
	private long sid;
	private long serid;
	private long caid;
	
	public long getId()
	{
		return id;
	}
	
	public void setId(long id)
	{
		this.id = id;
	}
	
	public long getSid()
	{
		return sid;
	}
	
	public void setSid(long sid)
	{
		this.sid = sid;
	}
	
	public long getSerid()
	{
		return serid;
	}
	
	public void setSerid(long serid)
	{
		this.serid = serid;
	}
	
	public long getCaid()
	{
		return caid;
	}
	
	public void setCaid(long caid)
	{
		this.caid = caid;
	}
	
	
}
